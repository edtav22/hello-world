saldo = 450
saque = 200

#saldo é igual a saque
print(saldo == saque)
#saldo é diferente de saque
print(saldo != saque)
#saldo é maior que saque
print(saldo > saque)
#saldo é maior ou igual que saque
print(saldo >= saque)
#saldo é menor que saque
print(saldo < saque)
#saldo é menor ou igual que saque
print(saldo <= saque)
